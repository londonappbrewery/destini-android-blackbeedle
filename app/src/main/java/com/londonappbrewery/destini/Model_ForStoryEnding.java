package com.londonappbrewery.destini;

public class Model_ForStoryEnding {

    int mending1;
    int mending2;
    int mending3;

    public Model_ForStoryEnding(int ending1, int ending2, int ending3) {
        mending1 = ending1;
        mending2 = ending2;
        mending3 = ending3;
    }

    public int getMending1() {
        return mending1;
    }

    public int getMending2() {
        return mending2;
    }

    public int getMending3() {
        return mending3;
    }
}
